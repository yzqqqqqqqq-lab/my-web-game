// 生成游戏slug的辅助函数
function generateSlug(provider: string, title: string): string {
  const providerSlug = provider.toLowerCase().replace(/\s+/g, '-');
  const titleSlug = title.toLowerCase().replace(/\s+/g, '-').replace(/&/g, 'and');
  return `${providerSlug}-${titleSlug}`;
}

